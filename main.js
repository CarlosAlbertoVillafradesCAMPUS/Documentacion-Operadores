let numero1 = 10;
let numero2 = 5

//AND Lógico (&&)
console.log(`AND Lógico (&&)`)
console.log(`\t((${numero1} == "${numero1}") && (${numero2} == "${numero2}") ) ${numero1==numero1 && numero2 == numero2}`)

//OR lógico (||)
console.log(`OR lógico (||)`)
console.log(`\t((${numero1} == ${numero2}) || (${numero1} > ${numero2}) ) ${numero1 == numero2 || numero1 > numero2}`)


//NOT lógico (!)
console.log(`NOT lógico (!)`)
console.log(`\t!true = ${!true}`)
console.log(`\t!false = ${!false}`)

